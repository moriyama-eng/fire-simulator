/**
 * Generate a dummy simulation result (runSimulation return type).
 * The final month values in totalPercentileData are aligned with finalMedian / final_p10_jpy.
 * Note: This is the return value of a single simulation run and does not include differences across levels.
 *
 * [⚠️ IMPORTANT - Mathematical invalidity of values]
 * Final values for non-50/10 percentiles (30, 70, 90) are expedient dummy values
 * generated by simple proportion: Math.round(finalMedian * (pct / 50)).
 * They do not represent statistically correct percentiles of log-normal or log-t distributions.
 * Do not perform any statistical validation using these values.
 * If tests directly validating these percentile values are added in the future,
 * they must be replaced with fixtures based on correct distributions.
 *
 * [Independence of References]
 * totalPercentileData, cashPercentileData, and ddPercentileData are
 * generated as separate arrays. Changing one in a test by mistake
 * will not affect the others.
 *
 * @param {object} overrides
 * @param {number} [overrides.finalMedian=538074816] - Final median assets
 * @param {number} [overrides.finalP10=39342408] - Final 10% percentile assets (corresponds to final_p10_jpy in makeBaseMetrics)
 * @param {number} [overrides.simYears=30] - Used to calculate dataLen
 */
export function makeDummySimResult(overrides = {}) {
  const pcts = [10, 30, 50, 70, 90];
  const simYears = overrides.simYears ?? 30;
  const dataLen = simYears * 12 + 1;
  const finalMedian = overrides.finalMedian ?? 538074816;
  const finalP10 = overrides.finalP10 ?? 39342408;

  const buildPercentileData = () => {
    return pcts.map((pct) => {
      const arr = new Float32Array(dataLen).fill(100_000_000);
      if (pct === 10) arr[dataLen - 1] = finalP10;
      else if (pct === 50) arr[dataLen - 1] = finalMedian;
      else arr[dataLen - 1] = Math.round(finalMedian * (pct / 50));
      return arr;
    });
  };

  // Generate independent arrays for total, cash, and dd respectively
  const totalPD = buildPercentileData();
  const cashPD = buildPercentileData();
  const ddPD = buildPercentileData();

  return {
    percentiles: pcts,
    totalPercentileData: totalPD,
    cashPercentileData: cashPD,
    ddPercentileData: ddPD,
    successRate: overrides.successRate ?? 93.23,
    finalMedian,
    worst10MaxDd: overrides.worst10MaxDd ?? -0.8054955005645752,
    worst5MaxDd: overrides.worst5MaxDd ?? -1,
    medianMaxUw: overrides.medianMaxUw ?? 102,
    worst10MaxUw: overrides.worst10MaxUw ?? 310,
    maxDdPerPath: new Float32Array(1000),
    maxUwPerPath: new Float32Array(1000),
    // v2.3.0: New metrics (default is all paths 0)
    belowInitPeriods: overrides.belowInitPeriods ?? new Float32Array(1000),
    consecutiveSellPeriods: overrides.consecutiveSellPeriods ?? new Float32Array(1000),
    dataLen,
    usedSeed: overrides.usedSeed ?? 123456,
    modelType: overrides.modelType ?? 'log-t',
    usedDf: overrides.usedDf ?? 4.2,
    params: { simPaths: 1000, totalMonths: dataLen - 1 },
    targetAssetMaintainRate: overrides.targetAssetMaintainRate ?? 93.23,
    targetAssetRatio: overrides.targetAssetRatio ?? 1.0,
  };
}

