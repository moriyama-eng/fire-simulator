let currentLang = (() => {
  try { return localStorage.getItem('lang'); } catch (e) { return null; }
})() || 'ja';

export function t(key, placeholders = []) {
  let text = TRANSLATIONS[currentLang]?.[key];
  // undefined または null の場合のみフォールバック（空文字は有効とする）
  if (text === undefined || text === null) {
    text = '[EN] ' + (TRANSLATIONS.ja[key] || key);
  }
  // text が空文字の場合はそのまま空文字を返す（フォールバックしない）

  text = text.replace(/\{VERSION\}/g, APP_VERSION);

  placeholders.forEach((p, i) => {
    text = text.replace(new RegExp('\\{' + i + '\\}', 'g'), p);
  });
  return text;
}

export function setLanguage(lang) {
  if (!['ja', 'en'].includes(lang)) return;
  currentLang = lang;
  try { localStorage.setItem('lang', lang); } catch (e) { }
  document.dispatchEvent(new CustomEvent('languageChanged'));
}

export function getLanguage() { return currentLang; }
export function getSupportedLanguages() { return ['ja', 'en']; }

export function formatCurrency(valueInJPY, displayUnit) {
  if (!isFinite(valueInJPY)) return currentLang === 'ja' ? '－' : 'N/A';
  if (valueInJPY < 0) return '-' + formatCurrency(-valueInJPY, displayUnit);

  if (currentLang === 'ja') {
    if (displayUnit === '億円') {
      return (valueInJPY / 1e8).toLocaleString('ja-JP', { maximumFractionDigits: 1, minimumFractionDigits: 1 }) + '億円';
    }
    if (displayUnit === '万円') {
      return (valueInJPY / 1e4).toLocaleString('ja-JP', { maximumFractionDigits: 0 }) + '万円';
    }
    return valueInJPY.toLocaleString('ja-JP');
  }

  // 英語モード：USD表示（固定レート $1 = 100円）
  // 表示ルール: 0→$0, 1-999→整数, 1,000-999,999→$X.XK, 1,000,000-999,999,999→$X.XM, 1e9以上→$X.XB
  // 四捨五入ポリシー: 1ドル未満の端数は四捨五入して整数ドルに丸める（例: 99円 → $1）
  var usd = valueInJPY / 100;
  if (usd === 0) return '$0';
  var absUsd = Math.abs(usd);
  var formatted;
  if (absUsd >= 1_000_000_000) {
    var val = usd / 1_000_000_000;
    var rounded = Number(val.toFixed(1));
    var formatted = rounded % 1 === 0 ? rounded.toString() : rounded.toFixed(1);
    return '$' + formatted + 'B';
  } else if (absUsd >= 1_000_000) {
    var val = usd / 1_000_000;
    var rounded = Number(val.toFixed(1));
    var formatted = rounded % 1 === 0 ? rounded.toString() : rounded.toFixed(1);
    return '$' + formatted + 'M';
  } else if (absUsd >= 1_000) {
    var val = usd / 1_000;
    var rounded = Number(val.toFixed(1));
    var formatted = rounded % 1 === 0 ? rounded.toString() : rounded.toFixed(1);
    return '$' + formatted + 'K';
  } else {
    var formatted = Math.round(usd).toString();
    return '$' + formatted;
  }
}

export function formatPercent(value) {
  return (value * 100).toFixed(1) + '%';
}

export function formatDate(date) {
  return date.toLocaleString(currentLang === 'ja' ? 'ja-JP' : 'en-US', { year: 'numeric', month: '2-digit', day: '2-digit', hour: '2-digit', minute: '2-digit' });
}

export function formatYears(years) {
  return currentLang === 'ja' ? years + '年' : years + ' years';
}

export function formatNumber(value, unitKey) {
  var formatted = currentLang === 'ja' ? value.toLocaleString('ja-JP') : value.toLocaleString('en-US');
  if (!unitKey) return formatted;
  var unit = t(unitKey);
  return formatted + ' ' + unit;
}
